  <section class="histoire">
    <div class="text">
      <h2>Histoire</h2>
      <p>L'EPISE à été créée par Mme Brigitte Gustin et l'ancien directeur de la MDE.<br>
Le directeur était sorti de son bureau et il a vu un étudiant qui attendait sur les canapé de l'accueil de la MDE et d'un coup l'étudiant s'est effondré ! <br>
Le directeur est directement intervenu et lui a demandé si ça allait bien et l'a questionné un peu jusqu'à ce que l'étudiant lui confie que ça faisait UNE SEMAINE qu'il n'avait pas mangé.<br>
Et là il s'est dit que ce n’était pas possible, qu’il devait faire quelque chose. </p>
<p>Alors il a contacté Mme Gustin et ils ont créé l'EPISE, dont la particularité est qu'elle ne peut être gérée que par les étudiants eux-mêmes, pour inciter les jeunes à s’entraider et améliorer la qualité de vie dans leur campus malgré les difficultés.
</p>

