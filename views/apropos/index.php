      <h2>Où se situe l’Epise ?</h2>
      <p>L’EPISE est située sur le campus de l’Université de la Nouvelle-Calédonie, à côté de la MDE et du Sunset Café, juste en bas des escaliers à côté des terrains de sport.</p>
      <p>Elle est facilement accessible aux étudiants et ouverte deux jours par semaine.</p>
