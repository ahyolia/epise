    <div class="text">
      <h2>Présentation</h2>
      <p>L’Épicerie Solidaire de l’UNC (EPISE) est une association gérée par des étudiants de l'université, qui soutient les étudiants du campus en leur proposant toutes sortes de produits alimentaires, hygièniques et autres matériels de première nécessité, et tout ça seulement à hauteur de 200f par an.</p>
      <p>Elle vise à améliorer leur quotidien tout en luttant contre la précarité étudiante.</p>
      <p>Des actions solidaires et collectives y sont organisées régulièrement. Mais ce n'est pas assez, l'EPISE souhaite être ouverte plus souvent et pour cela l'équipe de bénévoles doit s'agrandir.</p>
    </div>
  </div>
